const num= Number(prompt("Enter the No of between 1 to 7 to see the  Day you want"))

switch(num){
    case 1:
      console.log("sunday");
      break;

      case 2:
        console.log("Monday");
        break;
        case 3:
      console.log("Tuesday");
      break;

      case 4:
        console.log("Wednesday");
        break;
        case 5:
      console.log("Thursday");
      break;

      case 6:
        console.log("Friday");
        break;

        case 7:
      console.log("saturday");
      break;

      default :
      console.log("Enter the valid no.")

     
}